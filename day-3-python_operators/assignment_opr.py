# Assignment Operator

# Basic assignment
x = 10
print("x =", x)

# Addition assignment
x += 5
print("x += 5 =", x)

# Subtraction assignment
x -= 3
print("x -= 3 =", x)

# Multiplication assignment
x *= 2
print("x *= 2 =", x)

# Division assignment
x /= 4
print("x /= 4 =", x)

# Modulus assignment
x %= 3
print("x %= 3 =", x)

# Floor division assignment
x = 9
x //= 2
print("x = 9, then x //= 2 =", x)

# Exponentiation assignment
x **= 3
print("x **= 3 =", x)