# comparison operators in Python

a = 10
b = 20

print("a =", a)
print("b =", b)

# Equal to
print("a == b:", a == b)

# Not equal to
print("a != b:", a != b)

# Greater than
print("a > b:", a > b)

# Less than
print("a < b:", a < b)

# Greater than or equal to
print("a >= b:", a >= b)

# Less than or equal to
print("a <= b:", a <= b)

# Another example with equal values
c = 15
d = 15

print("c =", c)
print("d =", d)

print("c == d:", c == d)
print("c != d:", c != d)
print("c >= d:", c >= d)
print("c <= d:", c <= d)
